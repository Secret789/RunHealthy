package com.example.prubatrabajofinal.Model.Historial;

public class UbicacionModel {
    public int ubiId;
    public String ubiFec;
    public String ubiLat;
    public String ubiLon;
    public String UbiVel;


    public UbicacionModel(int ubiId, String ubiFec, String ubiLat, String ubiLon, String UbiVel){
        this.ubiId=ubiId;
        this.ubiFec=ubiFec;
        this.ubiLat=ubiLat;
        this.ubiLon=ubiLon;
        this.UbiVel=UbiVel;

    }

}
